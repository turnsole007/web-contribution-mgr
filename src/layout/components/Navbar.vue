  methods: {
    logout () {
      axios({
        method: 'POST',
        url: '/mgr/api/admin/logout'
      })
        .then(Response => {
          if (Response.data === 'logout success.') {
            // alert('成功退出!')
            Message({
              message: '成功退出',
              type: 'success',
              duration: 5 * 1000
            })
            sessionStorage.removeItem('user')
