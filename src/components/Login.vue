<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" auto-complete="on" label-position="left">

      <div class="title-container">
        <h3 class="title">Login Form</h3>
      </div>

      <el-form-item prop="username">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="username"
          v-model="loginForm.username"
          placeholder="Username"
          name="username"
          type="text"
          tabindex="1"
          auto-complete="on"
        />
      </el-form-item>

      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input
          :key="passwordType"
          ref="password"
          v-model="loginForm.password"
          :type="passwordType"
          placeholder="Password"
          name="password"
          tabindex="2"
          auto-complete="on"
          @keyup.enter.native="handleLogin"
        />
        <span class="show-pwd" @click="showPwd">
          <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
        </span>
      </el-form-item>

      <el-button :loading="loading" type="primary" style="width:100%;margin-bottom:30px;" @click.native.prevent="handleLogin">Login</el-button>

    </el-form>
  </div>
</template>

<script>
import axios from 'axios'
import JSEncrypt from 'jsencrypt'
import { Message } from 'element-ui'

export default {
  name: 'Login',
  data () {
    const validatePassword = (rule, value, callback) => {
      if (value.length < 6) {
        callback(new Error('The password can not be less than 6 digits'))
      } else {
        callback()
      }
    }
    return {
      loginForm: {
        username: '',
        password: ''
      },
      loginRules: {
        username: [{ required: true, trigger: 'blur' }],
        password: [{ required: true, trigger: 'blur', validator: validatePassword }]
      },
      loading: false,
      passwordType: 'password',
      redirect: undefined
    }
  },
  watch: {
    $route: {
      handler: function (route) {
        this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }
  },
  methods: {
    showPwd () {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    encryptedData (publicKey, data) {
      var encryptor = new JSEncrypt()
      encryptor.setPublicKey(publicKey)
      return encryptor.encrypt(data)
    },
    handleLogin () {
      var publicKey = 'MIGfMA0GCSqGSIb3DQEBAQUAA4GNADCBiQKBgQCkXeTacyYGFCGBhdgL6oQdK58Aask4i7TugkBYMFH8mv1/2NxBb8ncDb5YKeBk00kd0BUNc48Gi6WW5u52hQ+V3/5FsYB6lIw91P5D7epXT/SXX9ePH4HAQ8btMk6T/ogsn02ZYh+d3bVTQpyp3RQJQ8fM5mhCrz9JpxbpLbaHzQIDAQAB'
      this.loginForm.password = this.encryptedData(publicKey, this.loginForm.password)
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true
          axios.post('/mgr/api/admin/login', this.loginForm)
            .then((response) => {
              window.console.log(response)
              if (response.data === 'login success') {
                sessionStorage.setItem('user', this.loginForm.username)
                this.$router.push({ path: this.redirect || '/' })
                this.loading = false
                this.loginForm.password = '' // 清除密码
              } else {
                Message({
                  message: response.data,
                  type: 'error',
                  duration: 5 * 1000
                })
                this.loading = false
                this.loginForm.password = '' // 清除密码
              }
            }).catch(() => {
              this.loading = false
              this.loginForm.password = '' // 清除密码
            })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    }
  }
}
</script>

